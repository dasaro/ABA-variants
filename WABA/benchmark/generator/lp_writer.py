#!/usr/bin/env python3
"""
ASP Code Generation Utilities for WABA Benchmark Frameworks

Provides functions to write individual ASP predicates and save complete frameworks.
"""

from pathlib import Path
from typing import List, Set, Dict, Tuple, Optional


def write_assumption(atoms: List[str]) -> str:
    """Generate assumption/1 declarations.

    Args:
        atoms: List of assumption names (e.g., ['a1', 'a2', 'a3'])

    Returns:
        ASP code string with compact assumption declarations

    Example:
        write_assumption(['a1', 'a2', 'a3']) → "assumption(a1; a2; a3)."
    """
    if not atoms:
        return ""

    # Use compact form for readability
    return f"assumption({'; '.join(atoms)})."


def write_weight(weights: Dict[str, int]) -> str:
    """Generate weight/2 declarations.

    Args:
        weights: Dict mapping atom names to integer weights

    Returns:
        ASP code string with weight declarations, one per line

    Example:
        write_weight({'a1': 10, 'd1': 25}) →
        "weight(a1, 10).\\nweight(d1, 25)."
    """
    if not weights:
        return ""

    lines = [f"weight({atom}, {weight})." for atom, weight in sorted(weights.items())]
    return '\n'.join(lines)


def write_rule(rule_id: str, head: str, body: List[str]) -> str:
    """Generate a single rule with head and body declarations.

    Args:
        rule_id: Unique rule identifier (e.g., 'r1')
        head: Head atom name
        body: List of body atom names

    Returns:
        ASP code string with head/2 and body/2 declarations

    Example:
        write_rule('r1', 'd1', ['a1', 'a2']) →
        "head(r1, d1). % r1: d1 <- a1, a2
         body(r1, a1; r1, a2)."
    """
    if not body:
        # Empty body (fact)
        return f"head({rule_id}, {head}). % {rule_id}: {head}."

    # Compact body form
    body_compact = '; '.join([f"{rule_id}, {atom}" for atom in body])
    comment = f"% {rule_id}: {head} <- {', '.join(body)}"

    return f"head({rule_id}, {head}). {comment}\nbody({body_compact})."


def write_rules(rules: List[Tuple[str, str, List[str]]]) -> str:
    """Generate multiple rules.

    Args:
        rules: List of (rule_id, head, body_list) tuples

    Returns:
        ASP code string with all rules, separated by newlines
    """
    if not rules:
        return ""

    return '\n\n'.join([write_rule(rid, head, body) for rid, head, body in rules])


def write_contrary(contraries: Dict[str, str]) -> str:
    """Generate contrary/2 declarations (attack relation).

    Args:
        contraries: Dict mapping attacked assumption to attacker atom
                   (total function: each assumption must have exactly one contrary)

    Returns:
        ASP code string with contrary declarations, one per line

    Example:
        write_contrary({'a1': 'attacker1', 'a2': 'attacker2'}) →
        "contrary(a1, attacker1).\\ncontrary(a2, attacker2)."
    """
    if not contraries:
        return ""

    lines = [f"contrary({assumption}, {attacker})."
             for assumption, attacker in sorted(contraries.items())]
    return '\n'.join(lines)


def write_budget(budget_value: int) -> str:
    """Generate budget/1 declaration.

    Args:
        budget_value: Budget constraint value (non-negative integer)

    Returns:
        ASP code string with budget declaration

    Example:
        write_budget(100) → "budget(100)."
    """
    return f"budget({budget_value})."


def write_comment_header(framework_name: str,
                         description: str,
                         parameters: Dict[str, any]) -> str:
    """Generate framework header comment block.

    Args:
        framework_name: Name of the framework
        description: Brief description of the framework
        parameters: Dict of parameters (e.g., {'A': 10, 'R': 5, 'D': 3})

    Returns:
        ASP comment block with framework metadata
    """
    param_lines = '\n'.join([f'%% {key}: {value}' for key, value in sorted(parameters.items())])

    return f"""%%================================================================
%% WABA Benchmark Framework: {framework_name}
%%================================================================
%%
%% {description}
%%
%% Parameters:
{param_lines}
%%
%% Generated by WABA benchmark generator
%%================================================================

"""


def save_framework(predicates: Dict[str, str],
                   filepath: Path,
                   framework_name: str,
                   description: str = "",
                   parameters: Dict[str, any] = None) -> None:
    """Save a complete WABA framework to a .lp file.

    Args:
        predicates: Dict with keys 'assumptions', 'weights', 'rules', 'contraries', 'budget'
                   containing ASP code strings for each section
        filepath: Path to save the .lp file
        framework_name: Name of the framework (for header)
        description: Description for header
        parameters: Framework parameters for header

    Example:
        save_framework(
            {
                'assumptions': 'assumption(a1; a2; a3).',
                'weights': 'weight(a1, 10).\\nweight(a2, 20).',
                'rules': 'head(r1, d1).\\nbody(r1, a1; r1, a2).',
                'contraries': 'contrary(a1, d1).\\ncontrary(a2, d1).',
                'budget': 'budget(50).'
            },
            Path('benchmark/frameworks/test.lp'),
            'test_framework',
            'Test framework',
            {'A': 3, 'R': 1}
        )
    """
    parameters = parameters or {}

    # Build file content sections
    sections = []

    # Header
    sections.append(write_comment_header(framework_name, description, parameters))

    # Assumptions
    if predicates.get('assumptions'):
        sections.append("%% Assumptions")
        sections.append(predicates['assumptions'])
        sections.append("")

    # Weights
    if predicates.get('weights'):
        sections.append("%% Weights")
        sections.append(predicates['weights'])
        sections.append("")

    # Rules
    if predicates.get('rules'):
        sections.append("%% Rules")
        sections.append(predicates['rules'])
        sections.append("")

    # Contraries (attacks)
    if predicates.get('contraries'):
        sections.append("%% Contraries (Attack Relation)")
        sections.append(predicates['contraries'])
        sections.append("")

    # Budget
    if predicates.get('budget'):
        sections.append("%% Budget Constraint")
        sections.append(predicates['budget'])
        sections.append("")

    # Write to file
    filepath.parent.mkdir(parents=True, exist_ok=True)
    with open(filepath, 'w') as f:
        f.write('\n'.join(sections))

    print(f"✓ Generated: {filepath}")


# Utility functions for common patterns

def generate_atom_names(prefix: str, count: int, start: int = 1) -> List[str]:
    """Generate sequential atom names.

    Args:
        prefix: Atom name prefix (e.g., 'a', 'd', 'r')
        count: Number of atoms to generate
        start: Starting index (default 1)

    Returns:
        List of atom names

    Example:
        generate_atom_names('a', 5) → ['a1', 'a2', 'a3', 'a4', 'a5']
    """
    return [f"{prefix}{i}" for i in range(start, start + count)]


def generate_rule_chain(rule_prefix: str,
                       derived_prefix: str,
                       body_atoms: List[str],
                       depth: int) -> List[Tuple[str, str, List[str]]]:
    """Generate a chain of derivation rules.

    Args:
        rule_prefix: Prefix for rule IDs (e.g., 'r')
        derived_prefix: Prefix for derived atom names (e.g., 'd')
        body_atoms: List of base atoms to use in first rule
        depth: Number of derivation levels

    Returns:
        List of (rule_id, head, body) tuples

    Example:
        generate_rule_chain('r', 'd', ['a1', 'a2'], 3) →
        [('r1', 'd1', ['a1', 'a2']),
         ('r2', 'd2', ['d1', 'a2']),
         ('r3', 'd3', ['d2', 'a2'])]
    """
    rules = []
    current_body = body_atoms.copy()

    for i in range(1, depth + 1):
        rule_id = f"{rule_prefix}{i}"
        head = f"{derived_prefix}{i}"
        rules.append((rule_id, head, current_body.copy()))

        # For next iteration, use derived atom + last body atom
        if len(current_body) > 1:
            current_body = [head, current_body[-1]]
        else:
            current_body = [head]

    return rules
